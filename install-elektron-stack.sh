log()  { printf '\n\033[1;32m==>\033[0m %s\n' "$1"; }
warn() { printf '\n\033[1;33m!!\033[0m %s\n' "$1"; }
die()  { printf '\n\033[1;31mERROR:\033[0m %s\n' "$1"; exit 1; }

rand_hex()    { openssl rand -hex "$1"; }
rand_base64() { openssl rand -base64 "$1" | tr -d '=+/\n' | cut -c1-"$1"; }

# Auto-generate any secret left blank in the CONFIG block above.
# Track which ones were actually auto-generated (vs. supplied by you) so the
# final summary can say so explicitly instead of just silently using them.
GENERATED_SECRETS=""
[ -z "$JWT_SECRET" ]              && { JWT_SECRET="$(rand_hex 32)"; GENERATED_SECRETS="${GENERATED_SECRETS}JWT_SECRET "; }
[ -z "$FAUCET_WALLET_PASSPHRASE" ] && { FAUCET_WALLET_PASSPHRASE="$(rand_base64 32)"; GENERATED_SECRETS="${GENERATED_SECRETS}FAUCET_WALLET_PASSPHRASE "; }
[ -z "$FAUCET_DB_PASS" ]          && { FAUCET_DB_PASS="$(rand_base64 24)"; GENERATED_SECRETS="${GENERATED_SECRETS}FAUCET_DB_PASS "; }
[ -z "$FAUCET_DB_ROOT_PASS" ]     && { FAUCET_DB_ROOT_PASS="$(rand_base64 24)"; GENERATED_SECRETS="${GENERATED_SECRETS}FAUCET_DB_ROOT_PASS "; }
[ -z "$FAUCET_ADMIN_PASS" ]       && { FAUCET_ADMIN_PASS="$(rand_base64 16)"; GENERATED_SECRETS="${GENERATED_SECRETS}FAUCET_ADMIN_PASS "; }
# RPC_USER/RPC_PASSWORD are always auto-generated by rpcauth.py further down
# (there's no CONFIG variable for the RPC password -- rpcauth.py makes its
# own if you don't pass one, see step 3 below), so it's always "generated".

