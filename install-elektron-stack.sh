FAUCET_ADMIN_USER="admin"
# leave empty to auto-generate (min 10 chars required by the faucet):
FAUCET_ADMIN_PASS=""
FAUCET_HCAPTCHA_SITE=""                       # from hcaptcha.com -- strongly recommended
